    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
     String id = txtAccountID.getText();

    // 1. Validation
    if (!id.matches("\\d{4}")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid 4-digit Account ID!");
        return;
    }

    // 2. Security Check: Load details if fields are empty 
    // This ensures the admin sees WHO they are deleting first.
    if (txtFirstName.getText().isEmpty()) {
        systems.loadToFields(id, txtFirstName, txtLastName, txtEmail, txtPhone, txtBalance);
        JOptionPane.showMessageDialog(this, "Review details carefully before deleting.");
    } 
    // 3. Confirm and Delete
    else {
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete Account " + id + "?", 
            "Confirm Deletion", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            if (systems.deleteAccount(id)) {
                JOptionPane.showMessageDialog(this, "Account deleted successfully!");
                clearFields(); // Reset the UI
            } else {
                JOptionPane.showMessageDialog(this, "Delete failed! ID might not exist.");
            }
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

