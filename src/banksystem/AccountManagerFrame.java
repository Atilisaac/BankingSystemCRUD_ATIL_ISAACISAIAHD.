    private void btnWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWithdrawActionPerformed
       // 1. Get values using your variable names
    String id = txtTransactAccId.getText();
    String amtStr = txtAmount.getText();
    String type = cmbTransactType.getSelectedItem().toString();

    // 2. Validation
    if (id.isEmpty() || amtStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in Account ID and Amount!");
        return;
    }

    try {
        // 3. Process the transaction through the System layer
        // We pass "Withdraw" as the last parameter
        systems.performTransaction(id, type, amtStr, "Withdraw");
        
        // 4. Clear the amount field after a successful click
        txtAmount.setText("");
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnWithdrawActionPerformed

    private void btnDepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositActionPerformed
        // 1. Get values using your specific variable names
    String id = txtTransactAccId.getText();
    String amtStr = txtAmount.getText();
    
    // Get the account type (Savings/Current) from your ComboBox
    String type = cmbTransactType.getSelectedItem().toString();

    // 2. Simple Validation
    if (id.isEmpty() || amtStr.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please enter Account ID and Amount!");
        return;
    }

    // 3. Call the system logic
    // We pass "Deposit" to tell the DAO to ADD money
    systems.performTransaction(id, type, amtStr, "Deposit");
    
    // 4. Clear amount field for the next transaction
    txtAmount.setText("");
    }//GEN-LAST:event_btnDepositActionPerformed

    private void btnTransExecuteSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTransExecuteSearchActionPerformed
        // 1. Grab selection and input from your unique components
    String filterType = cmbTransFilterType.getSelectedItem().toString();
    String searchValue = txtTransSearchValue.getText().trim();

    // 2. Logic check
    if (searchValue.isEmpty()) {
        // If empty, reset the table to show all transactions
        systems.populateTransactionTable(tblTransactions);
    } else {
        // 3. Apply the dynamic filter using your existing table name
        systems.applyTransactionFilter(tblTransactions, filterType, searchValue);
    }
    }//GEN-LAST:event_btnTransExecuteSearchActionPerformed

