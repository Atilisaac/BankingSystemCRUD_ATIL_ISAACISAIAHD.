    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
     String id = txtAccountID.getText(); // The 4-digit ID

    // Validation: ID must be 4 digits
    if (!id.matches("\\d{4}")) {
        JOptionPane.showMessageDialog(this, "Please enter a valid 4-digit Account ID!");
        return;
    }

    // STEP 1: If the name field is empty, the admin hasn't searched yet.
    // We load the data so they can see what they are updating.
    if (txtFirstName.getText().isEmpty()) {
        systems.loadToFields(id, txtFirstName, txtLastName, txtEmail, txtPhone, txtBalance);
    } 
    // STEP 2: If info is already there, we save the new changes.
    else {
        String fn = txtFirstName.getText();
        String ln = txtLastName.getText();
        String em = txtEmail.getText();
        String ph = txtPhone.getText();

        if (systems.updateCustomerInfo(id, fn, ln, em, ph)) {
            JOptionPane.showMessageDialog(this, "Update Successful!");
            clearFields(); // Reset UI after success
        } else {
            JOptionPane.showMessageDialog(this, "Update Failed. Check database connection.");
        }
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

